public class leftshiftandrightshift {
	public static void main(String[] args) {
    // a = 5(00000101), b = 9(00001001)
     int  a = 00000101; 
    int b = 00001001;
    // The result is 00001010
	System.out.println(a<<1 );
   
    // The result is 00010010
    System.out.println(b<<1 ); 

}
}
